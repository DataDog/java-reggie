  /** Scalar fallback for finding next byte. */
  private static int findNextByteScalar(String input, int fromIndex, int len, byte target) {
    for (int i = fromIndex; i < len; i++) {
      char c = input.charAt(i);
      if (c == (target & 0xFF)) {
        return i;
      }
    }
    return -1;
  }

