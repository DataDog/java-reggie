  /**
   * Count consecutive matches of a single character starting from a position. Uses scalar
   * implementation for now (SWAR TODO for future optimization).
   *
   * @param input input string
   * @param start starting position
   * @param len string length
   * @param target character to match
   * @return number of consecutive matches from start position
   */
  public static int countMatchingSingleByte(String input, int start, int len, char target) {
    int count = 0;
    int pos = start;

    while (pos < len) {
      char c = input.charAt(pos);
      if (c != target) {
        break;
      }
      count++;
      pos++;
    }

    return count;
  }

  /**
   * Count consecutive matches in a character range [min, max] starting from a position. Uses scalar
   * implementation for now (SWAR TODO for future optimization).
   *
   * @param input input string
   * @param start starting position
   * @param len string length
   * @param min lower bound (inclusive)
   * @param max upper bound (inclusive)
   * @return number of consecutive matches from start position
   */
  public static int countMatchingRange(String input, int start, int len, char min, char max) {
    int count = 0;
    int pos = start;

    while (pos < len) {
      char c = input.charAt(pos);
      if (c < min || c > max) {
        break;
      }
      count++;
      pos++;
    }

    return count;
  }
