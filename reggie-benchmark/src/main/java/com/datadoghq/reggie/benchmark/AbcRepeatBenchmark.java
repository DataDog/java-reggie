package com.datadoghq.reggie.benchmark;

import com.datadoghq.reggie.Reggie;
import com.datadoghq.reggie.runtime.ReggieMatcher;
import java.util.concurrent.TimeUnit;
import java.util.regex.Pattern;
import org.openjdk.jmh.annotations.Benchmark;
import org.openjdk.jmh.annotations.BenchmarkMode;
import org.openjdk.jmh.annotations.Fork;
import org.openjdk.jmh.annotations.Measurement;
import org.openjdk.jmh.annotations.Mode;
import org.openjdk.jmh.annotations.OutputTimeUnit;
import org.openjdk.jmh.annotations.Scope;
import org.openjdk.jmh.annotations.Setup;
import org.openjdk.jmh.annotations.State;
import org.openjdk.jmh.annotations.Warmup;

/** Baseline vs JDK for (?:abc){100} on a 301-char input. */
@BenchmarkMode(Mode.Throughput)
@OutputTimeUnit(TimeUnit.MILLISECONDS)
@Warmup(iterations = 5, time = 1)
@Measurement(iterations = 5, time = 1)
@Fork(3)
@State(Scope.Thread)
public class AbcRepeatBenchmark {
  private Pattern jdk;
  private ReggieMatcher reggie;
  private String input;

  @Setup
  public void setup() {
    jdk = Pattern.compile("(?:abc){100}");
    reggie = Reggie.compile("(?:abc){100}");
    input = "x" + "abc".repeat(100) + "y";
  }

  @Benchmark
  public boolean jdkFind() {
    return jdk.matcher(input).find();
  }

  @Benchmark
  public boolean reggieFind() {
    return reggie.find(input);
  }
}
