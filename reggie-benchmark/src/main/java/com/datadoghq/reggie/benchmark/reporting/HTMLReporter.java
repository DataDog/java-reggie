  private String generateSummary(JMHResults results, Map<String, Comparison> comparisons) {
    // Discover all implementations in results
    Set<String> allImpls =
        comparisons.values().stream()
            .flatMap(c -> c.getImplementations().stream())
            .collect(Collectors.toCollection(LinkedHashSet::new));

    StringBuilder statsHtml = new StringBuilder();
    statsHtml.append(
        String.format(
            """
                    <div class="stat-card">
                        <div class="stat-label">Total Benchmarks</div>
                        <div class="stat-value">%d</div>
                    </div>
            """,
            results.size()));

    // Generate speedup stats vs each non-reggie implementation
    for (String impl : allImpls) {
      if (impl.equals("reggie") || impl.equals("other")) continue;

      double avgSpeedup =
          comparisons.values().stream()
              .filter(c -> c.hasComparison() && c.scores.containsKey(impl))
              .mapToDouble(c -> c.getSpeedup(impl))
              .average()
              .orElse(1.0);

      long fasterCount =
          comparisons.values().stream()
              .filter(
                  c -> c.hasComparison() && c.scores.containsKey(impl) && c.getSpeedup(impl) > 1.0)
              .count();

      long totalWithImpl =
          comparisons.values().stream()
              .filter(c -> c.hasComparison() && c.scores.containsKey(impl))
              .count();

      statsHtml.append(
          String.format(
              """
                    <div class="stat-card speedup">
                        <div class="stat-label">vs %s Speedup</div>
                        <div class="stat-value">%.2fx</div>
                    </div>
                    <div class="stat-card">
                        <div class="stat-label">Reggie > %s</div>
                        <div class="stat-value">%d/%d</div>
                    </div>
                """,
              impl.toUpperCase(), avgSpeedup, impl.toUpperCase(), fasterCount, totalWithImpl));
    }

    return String.format(
        """
            <div class="summary">
                <h2>Summary</h2>
                <div class="stat-grid">
                    %s
                </div>
            </div>
            """,
        statsHtml.toString());
  }

  private String generateComparisonTable(Map<String, Comparison> comparisons) {
    // Discover all implementations
    Set<String> allImpls =
        comparisons.values().stream()
            .flatMap(c -> c.getImplementations().stream())
            .filter(impl -> !impl.equals("other"))
            .collect(Collectors.toCollection(LinkedHashSet::new));

    // Build header with dynamic columns
    StringBuilder headerCells = new StringBuilder("<th>Benchmark</th>");
    for (String impl : allImpls) {
      headerCells.append(
          String.format(
              "<th>%s (ops/ms)</th>", impl.substring(0, 1).toUpperCase() + impl.substring(1)));
    }
    // Add speedup columns for each non-reggie impl
    for (String impl : allImpls) {
      if (!impl.equals("reggie")) {
        headerCells.append(String.format("<th>vs %s</th>", impl.toUpperCase()));
      }
    }

    StringBuilder rows = new StringBuilder();
    for (Comparison comp : comparisons.values()) {
      if (!comp.hasComparison()) continue;

      StringBuilder row = new StringBuilder();
      row.append(String.format("<td>%s</td>", comp.name));

      // Score columns
      for (String impl : allImpls) {
        double score = comp.getScore(impl);
        if (score > 0) {
          row.append(String.format("<td class=\"metric\">%.0f</td>", score));
        } else {
          row.append("<td class=\"metric\">-</td>");
        }
      }

      // Speedup columns
      for (String impl : allImpls) {
        if (!impl.equals("reggie")) {
          double score = comp.getScore(impl);
          if (score > 0) {
            double speedup = comp.getSpeedup(impl);
            String speedupClass = speedup >= 1.0 ? "positive" : "negative";
            String badge =
                speedup >= 1.0
                    ? String.format("<span class='speedup-badge'>%.2fx</span>", speedup)
                    : String.format("<span class='slower-badge'>%.2fx</span>", 1.0 / speedup);
            row.append(String.format("<td class=\"metric %s\">%s</td>", speedupClass, badge));
          } else {
            row.append("<td class=\"metric\">-</td>");
          }
        }
      }

      rows.append(String.format("<tr>%s</tr>\n", row.toString()));
    }

    String implList =
        String.join(
            " vs ",
            allImpls.stream().map(s -> s.substring(0, 1).toUpperCase() + s.substring(1)).toList());

    return String.format(
        """
            <h2 style="margin-bottom: 20px; color: #667eea;">%s Performance</h2>
            <table>
                <thead>
                    <tr>
                        %s
                    </tr>
                </thead>
                <tbody>
                    %s
                </tbody>
            </table>
            """,
        implList, headerCells.toString(), rows.toString());
  }

  private String generateTable(JMHResults results) {
    StringBuilder rows = new StringBuilder();
    for (JMHResults.BenchmarkResult result : results.getResults()) {
      rows.append(
          String.format(
              """
                    <tr>
                        <td>%s</td>
                        <td class="metric">%.2f</td>
                        <td class="metric">%.2f</td>
                        <td>%s</td>
                    </tr>
                """,
              result.benchmark(), result.score(), result.error(), result.unit()));
    }

    return String.format(
        """
            <details>
                <summary>📋 Detailed Results (All Benchmarks)</summary>
                <table style="margin-top: 20px;">
                    <thead>
                        <tr>
                            <th>Benchmark</th>
                            <th>Throughput (ops/ms)</th>
                            <th>Error (±)</th>
                            <th>Unit</th>
                        </tr>
                    </thead>
                    <tbody>
                        %s
                    </tbody>
                </table>
            </details>
            """,
        rows.toString());
  }

  private String generateChartJS(Map<String, Comparison> comparisons) {
    List<Comparison> withComparison =
        comparisons.values().stream().filter(Comparison::hasComparison).collect(toList());

    // Discover all implementations
    Set<String> allImpls =
        withComparison.stream()
            .flatMap(c -> c.getImplementations().stream())
            .filter(impl -> !impl.equals("other"))
            .collect(Collectors.toCollection(LinkedHashSet::new));

    // Build labels
    StringBuilder labels = new StringBuilder();
    for (int i = 0; i < withComparison.size(); i++) {
      Comparison comp = withComparison.get(i);
      String shortName =
          comp.name.length() > 30 ? comp.name.substring(comp.name.lastIndexOf('.') + 1) : comp.name;
      labels.append("'").append(shortName.replace("'", "\\'")).append("'");
      if (i < withComparison.size() - 1) {
        labels.append(", ");
      }
    }

    // Build datasets for each implementation
    StringBuilder datasets = new StringBuilder();
    int implIdx = 0;
    for (String impl : allImpls) {
      String[] colors =
          IMPL_COLORS.getOrDefault(
              impl, new String[] {"rgba(128, 128, 128, 0.7)", "rgba(128, 128, 128, 1)"});
      String displayName =
          impl.equals("jdk")
              ? "JDK Pattern"
              : impl.substring(0, 1).toUpperCase() + impl.substring(1);

      StringBuilder data = new StringBuilder();
      for (int i = 0; i < withComparison.size(); i++) {
        Comparison comp = withComparison.get(i);
        data.append(comp.getScore(impl));
        if (i < withComparison.size() - 1) {
          data.append(", ");
        }
      }

      datasets.append(
          String.format(
              """
                    {
                        label: '%s',
                        data: [%s],
                        backgroundColor: '%s',
                        borderColor: '%s',
                        borderWidth: 2
                    }""",
              displayName, data.toString(), colors[0], colors[1]));

      if (implIdx < allImpls.size() - 1) {
        datasets.append(", ");
      }
      implIdx++;
    }

    return String.format(
        """
            const ctx = document.getElementById('comparisonChart').getContext('2d');
            new Chart(ctx, {
                type: 'bar',
                data: {
                    labels: [%s],
                    datasets: [%s]
                },
                options: {
                    responsive: true,
                    scales: {
                        y: {
                            beginAtZero: true,
                            title: { display: true, text: 'Operations per millisecond' }
                        },
                        x: {
                            ticks: { maxRotation: 45, minRotation: 45 }
                        }
                    },
                    plugins: {
                        legend: { display: true },
                        title: {
                            display: true,
                            text: 'Throughput Comparison (Higher is Better)',
                            font: { size: 16 }
                        }
                    }
                }
            });
            """,
        labels.toString(), datasets.toString());
  }

